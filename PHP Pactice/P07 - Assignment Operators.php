<?php

// Assignment Operators

// x = y The left operand gets set to the value of the experssion on the right.
// x += y Addition.
// x -= y Subtraction.
// x *= y Multiplication.
// x /= y Division.
// x %= y Modulus.



// x = y The left operand gets set to the value of the experssion on the right. Example
echo ("Assignment Operator. <br>");
$x = 10;
echo ("left operand: x = $x. <br>");



// x += y Addition
$y = 10;
$y += 10;
echo ("Addition: y += $y. <br>");


// x -= y Subtraction.
$sub_1 = 10;
$sub_1 -= 5;
echo ("Subtraction: y -= $sub_1. <br>");



// x *= y Multiplication.
$mul_1 = 10;
$mul_1 *= 5;
echo ("Multiplication: y *= $mul_1. <br>");



// x /= y Division.
$divi_1 = 12;
$divi_1 /= 3;
echo ("Division: y /= $divi_1. <br>");



// x %= y Modulus.
$modu_1 = 15;
$modu_1 %= 4;
echo ("Modulus: y %= $modu_1.");

?>