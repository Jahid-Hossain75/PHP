<?php

// Comparison Operators 

// == Equal.
// === Identical.
// != Not equal.
// <> Not equal.
// !== Not identical.
// > Greater than.
// < Less than.
// >= Greater than or equal to.
// <= Less than or equal to.



$num1 = 10;
$num2 = 12;
// == Equal.
echo ("Equal: num1 = 10 == num2 = 12 <br>");
var_dump($num1 == $num2);
echo ("<br><br>");



// === Identical.
echo ("Identical: num1 = 10 === num2 = 12 <br>");
var_dump($num1 === $num2);
echo ("<br><br>");




// != Not equal.
echo ("Not equal: num1 = 10 != num2 = 12 <br>");
var_dump($num1 != $num2);
echo ("<br><br>");




// <> Not equal.
echo ("Not equal: num1 = 10 <> num2 = 12 <br>");
var_dump($num1 <> $num2);
echo ("<br><br>");




// !== Not identical.
echo ("Not identical: num1 = 10 !== num2 = 12 <br>");
var_dump($num1 !== $num2);
echo ("<br><br>");




// > Greater than.
echo ("Greater than: num1 = 10 > num2 = 12 <br>");
var_dump($num1 > $num2);
echo ("<br><br>");


// < Less than.
echo ("Less than: num1 = 10 < num2 = 12 <br>");
var_dump($num1 < $num2);
echo ("<br><br>");



// >= Greater than or equal to.
echo ("Greater than or equal to: num1 = 10 >= num2 = 12 <br>");
var_dump($num1 >= $num2);
echo ("<br><br>");



// <= Less than or equal to.
echo ("Less than or equal to: num1 = 10 <= num2 = 12 <br>");
var_dump($num1 <= $num2);
echo ("<br><br>");


?>