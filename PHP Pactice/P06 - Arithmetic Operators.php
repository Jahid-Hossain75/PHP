<?php

//Arithmetic operators

//  +  Addition, Example $x + $y.
//  -  Subtraction, Example $x - $y.
//  *  Multiplication, Example $x * $y.
//  /  Division, Example $x / $y.
//  %  Modulus, Example $x % $y.
//  **  Exponentioation Example $x ** $y.

// +  Addition, Example
echo ("Arithmetic Operator. <br>");
$frist_num = 15;
$second_num = 12;

$sum = $frist_num + $second_num;
echo ("Sum of: $frist_num + $second_num = $sum. <br>");


//  -  Subtraction, Example
$sub = $frist_num - $second_num;
echo ("Sub of: $frist_num - $second_num = $sub. <br>");


// *  Multiplication, Example
$mul = $frist_num * $second_num;
echo ("Mul of: $frist_num * $second_num = $mul. <br>");


//  /  Division, Example
$divi = $frist_num / $second_num;
echo ("Division of: $frist_num / $second_num = $divi. <br>");


//  %  Modulus, Example
$modu = $frist_num % $second_num;
echo ("Modulus of: $frist_num % $second_num = $modu. <br>");


//  **  Exponentioation Example
$expon = $frist_num ** $second_num;
echo ("Expon of: $frist_num ** $second_num = $expon.");



