<?php

//Increment / Decrement Operators

// ++$x Pre-increment.
// $x++ Post-increment.
// --$x Pre-decrement.
// $x-- Post-decrement.



// ++$x Pre-increment.
echo ("Increment / Decrement Operators. <br>");
$x = 10;
echo ("Pre-increment: ++x = ".++$x."<br>");



// $x++ Post-increment.
$y = 10;
echo ("Post-increment: x++ = ".$y++."<br>");




// --$x Pre-decrement.
$z = 15;
echo ("Pre-decrement: --x = ".--$z."<br>");




// $x-- Post-decrement.
$u = 15;
echo ("Post-decrement: x-- = ".$u--."<br>");

?>